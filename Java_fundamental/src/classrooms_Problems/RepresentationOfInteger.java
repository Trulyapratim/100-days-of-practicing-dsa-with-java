package classrooms_Problems;

public class RepresentationOfInteger {
    public static void main() {
        int a = 134;         // decimal or base - 10 representation
        System.out.println(a);
        int b = 0123;         // octal or base - 8 representation
        System.out.println(b);
        int c = 0b111;          // binary or base - 2 rep
        System.out.println(c);
        int d = 0xAB;           // hexadecimal or base-16 rep
        System.out.println(d);
    }
}
