package classrooms_Problems;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("A\bB");
        System.out.println(" ");
        System.out.println("s\tB");
        System.out.println("\nL");
        System.out.println("\fF");
        System.out.println("\rc");
        System.out.println("\\");
        System.out.println("\"");
        System.out.println("\'");
    }
}
