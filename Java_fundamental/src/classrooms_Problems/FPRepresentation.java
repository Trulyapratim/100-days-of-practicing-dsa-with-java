package classrooms_Problems;

public class FPRepresentation {
    public static void main(String[] args) {
        float f1 = 0b1101;          // in binary
        System.out.println(f1);
        float f2 = 0171;   // octal
        System.out.println(f2);
        float f3 = 0xface;     // hexadecimal
        System.out.println(f3);
        float f4 = 123434345;     // got converted into scientific notation
        System.out.println(f4);
    }
}
